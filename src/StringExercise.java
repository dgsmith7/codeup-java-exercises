public class StringExercise {
    public static void main(String[] args) {
        String l1 = "We don't need no education\nWe don't need no thought control";
        System.out.println(l1);
        String l2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(l2);
        String l3 = "In windows, the main drive is usually C:\\";
        System.out.println(l3);
        String l4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(l4);
    }
}
